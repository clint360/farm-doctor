interface I18nContextType {
  lang: Lang;
  t: (key: string) => string;
  setLang: (lang: Lang) => void;
  toggleLang: () => void;
}

const I18nContext = createContext<I18nContextType>({
  lang: "en",
  t: (k) => k,
  setLang: () => {},
  toggleLang: () => {},
});

const LANG_KEY = "fd_lang";

function readStoredLang(): Lang {
  if (typeof window === "undefined") return "en";
  try {
    const v = localStorage.getItem(LANG_KEY);
    if (v === "en" || v === "fr" || v === "pid") return v;
  } catch {}
  return "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => { setLangState(readStoredLang()); }, []);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    try { localStorage.setItem(LANG_KEY, l); } catch {}
  }, []);

  const t = useCallback(
    (key: string) => T[lang]?.[key] ?? T.en[key] ?? key,
    [lang]
  );

  const toggleLang = useCallback(() => {
    setLang(lang === "en" ? "fr" : lang === "fr" ? "pid" : "en");
  }, [lang, setLang]);

  return (
    <I18nContext.Provider value={{ lang, t, setLang, toggleLang }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
